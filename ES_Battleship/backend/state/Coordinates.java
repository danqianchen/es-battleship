package backend.state;

public class Coordinates {
	
	private int x;
	private int y;
	
	/**
	 * Get the X coordinate.
	 * @return
	 */
	public int getX() {
		return x;
	}
	
	/**
	 * Set the X coordinate.
	 * @param x
	 */
	public void setX(int x) {
		this.x = x;
	}
	
	/**
	 * Get the Y coordinate.
	 * @return
	 */
	public int getY() {
		return y;
	}
	
	/**
	 * Set the Y coordinate.
	 * @param y
	 */
	public void setY(int y) {
		this.y = y;
	}
}
